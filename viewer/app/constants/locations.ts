/**
 * Search locations by name (case-insensitive, partial match)
 */
export const searchLocations = (
  query: string,
  limit: number = 10,
): Location[] => {
  if (!query.trim()) return [];

  const normalizedQuery = query.toLowerCase().trim();

  return LOCATIONS.filter((loc) => {
    const nameMatch = loc.name.toLowerCase().includes(normalizedQuery);
    const countryMatch = loc.country?.toLowerCase().includes(normalizedQuery);
    return nameMatch || countryMatch;
  })
    .sort((a, b) => {
      // Prioritize exact matches
      const aExact = a.name.toLowerCase() === normalizedQuery;
      const bExact = b.name.toLowerCase() === normalizedQuery;
      if (aExact && !bExact) return -1;
      if (!aExact && bExact) return 1;

      // Then prioritize starts-with matches
      const aStarts = a.name.toLowerCase().startsWith(normalizedQuery);
      const bStarts = b.name.toLowerCase().startsWith(normalizedQuery);
      if (aStarts && !bStarts) return -1;
      if (!aStarts && bStarts) return 1;

      // Then sort by type (cities first, then countries, then regions)
      const typeOrder = { landmark: 0, city: 1, country: 2, region: 3 };
      return typeOrder[a.type] - typeOrder[b.type];
    })
    .slice(0, limit);
};
